% Variables

% defining x and initializing it with a value
x = 3;

% defining x and initializing it with an expression
x = sqrt(16);

% When an expression returns a result that is not
% assigned to any variable, the system assigns it
% to a variable named ans, which can be used later
sqrt(78)

x = 7 * 8;
y = x * 7.89

% You can have multiple assignments on the same line.
a = 2; b = 7; c = a * b

% Long assignments can be extended to another line
% by using an ellipses (...)
initial_velocity = 0;
acceleration = 9.8;
time = 20;
final_velocity = ...
initial_velocity + ...
acceleration * ...
time ...

